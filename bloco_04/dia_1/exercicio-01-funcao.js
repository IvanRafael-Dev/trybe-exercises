/* variaves */

function soma(num1, num2) {
    let res = num1 + num2;
    return res;
}

function subtrai(num1, num2) {
    let res = num1 - num2;
    return res;
}

function multiplica(num1, num2) {
    let res = num1 * num2;
    return res;
}

function divide(num1, num2) {
    let res = num1 / num2;
    return res;
}

function modulo(num1, num2) {
    let res = num1 % num2;
    return res;
}


